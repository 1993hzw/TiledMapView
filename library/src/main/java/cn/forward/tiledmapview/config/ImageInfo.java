package cn.forward.tiledmapview.config;

public class ImageInfo {
    private int mWidth;

    private int mHeight;

    public ImageInfo(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
